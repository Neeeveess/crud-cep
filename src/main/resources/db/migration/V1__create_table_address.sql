CREATE TABLE address (
    CEP VARCHAR(255) PRIMARY KEY,
    LOGRADOURO VARCHAR(255),
    COMPLEMENTO VARCHAR(255),
    UNIDADE VARCHAR(255),
    BAIRRO VARCHAR(255),
    LOCALIDADE VARCHAR(255),
    UF VARCHAR(255),
    IBGE VARCHAR(255),
    GIA VARCHAR(255),
    DDD VARCHAR(255),
    SIAFI VARCHAR(255)
);